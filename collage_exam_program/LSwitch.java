class LSwitch {
    public static void main(String[] args) {
        int day = 3;

        switch(day) {
            case 1:
                System.out.println("Saterday");
                break;
            case 2:
                System.out.println("Saterday");
                break;
            default:
                System.out.println("invalid");
        }
    }
}